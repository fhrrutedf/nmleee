const { PrismaClient } = require('@prisma/client');

const prisma = new PrismaClient();

async function main() {
    const email = "ahmad@test.com";

    // 1. Get the admin user
    const admin = await prisma.user.findUnique({ where: { email } });
    if (!admin) {
        console.error("ADMIN_NOT_FOUND");
        return;
    }

    // 2. Create the real blog post
    const post = await prisma.blogPost.create({
        data: {
            title: "فن اختيار المنتج الرقمي: كيف تلاقي فكرة يدفع الناس لأجلها؟",
            slug: "choosing-winning-digital-product-idea-2026",
            content: `
أسمع الكثير من المدربين يقولون: "عندي فكرة كورس خرافية، لكن لا أحد يشتري". الحقيقة المرة التي لا يحب أحد سماعها هي أن جمهورك لا يهتم بـ "فكرتك"، بل يهتم بـ "مشكلته".

لقد رأيت مئات المنتجات الرقمية تفشل، ليس لأنها سيئة، بل لأنها كانت "حلاً يبحث عن مشكلة". إليك كيف تختار فكرة منتج رقمي يجعلك تربح وأنت نائم:

### 1. ابحث عن "الوجع" وليس "الإعجاب"
لا تسأل الناس: "هل تعجبكم هذه الفكرة؟" (الكل سيجامل بكلمة نعم). اسأل نفسك: "ما هو الشيء الذي يجعل الناس يسهرون قلقين في الليل؟".
*   إذا كان جمهورك مبرمجين، فـ "الوجع" هو (كيف أجتاز مقابلة جوجل؟) وليس (أساسيات لغة بايثون).
*   إذا كان جمهورك في سوريا، فـ "الوجع" هو (كيف أستلم أرباحي بالدولار؟) وليس (مقدمة في التجارة الإلكترونية).

### 2. قاعدة الـ 10% (The 10% Rule)
لا تحاول تعليم الناس "كل شيء". المدرب الذكي هو من يحل 10% فقط من المشكلة الكبيرة، لكنه يحلها بجودة 100%. 
*   بدل "دورة كاملة عن التصميم"، اصنع "قالب بورتفوليو احترافي للمصممين". أيهما أسرع في البيع؟ طبعاً القالب.

### 3. "البرهان" قبل "البيان"
الناس في 2026 لا يشترون وعوداً. إذا أردت بيع منتج رقمي، ابدأ بتقديم "نتيجة" مجانية صغيرة. 
*   مقال تعليمي واحد يحل مشكلة تقنية معقدة، سيبني لك ثقة تعادل حملة إعلانية بآلاف الدولارات.

---
المنصات (مثل منصتي الرقمية وغيرها) هي مجرد "أدوات". السر الحقيقي ليس في الأداة التي تستخدمها، بل في "القيمة" التي تضعها داخل هذه الأداة. ابدأ بالبحث عن "الثغرة" في حياة جمهورك، وسيكون منتجك هو "الجسر" الذي يحتاجونه.
      `,
            excerpt: "الفكرة ليست هي الكنز.. الاحتياج هو الكنز الحقيقي. تعلم كيف تكتشف ما يحتاجه جمهورك فعلياً وتحوله إلى أرباح مستدامة.",
            coverImage: "https://images.unsplash.com/photo-1519389950473-47ba0277781c?w=1200",
            category: "تحليلات",
            authorName: "ماهر",
            status: "PUBLISHED",
            userId: admin.id
        }
    });

    console.log("POST_PUBLISHED_SUCCESSFULLY:", post.slug);
}

main()
    .catch(e => { console.error(e); process.exit(1); })
    .finally(async () => { await prisma.$disconnect(); });
