import { NextResponse } from 'next/server';
import { prisma } from '@/lib/db';

export async function GET() {
    try {
        const admin = await prisma.user.findUnique({ where: { email: 'ahmad@test.com' } });
        if (!admin) return NextResponse.json({ error: 'Admin not found' }, { status: 404 });

        const post = await prisma.blogPost.upsert({
            where: { slug: 'choosing-winning-digital-product-idea-2026' },
            update: { status: 'PUBLISHED' },
            create: {
                title: "فن اختيار المنتج الرقمي: كيف تلاقي فكرة يدفع الناس لأجلها؟",
                slug: "choosing-winning-digital-product-idea-2026",
                content: `
<p>أسمع الكثير من المدربين يقولون: "عندي فكرة كورس خرافية، لكن لا أحد يشتري". الحقيقة المرة التي لا يحب أحد سماعها هي أن جمهورك لا يهتم بـ "فكرتك"، بل يهتم بـ "مشكلته".</p>

<p>لقد رأيت مئات المنتجات الرقمية تفشل، ليس لأنها سيئة، بل لأنها كانت "حلاً يبحث عن مشكلة". إليك كيف تختار فكرة منتج رقمي يجعلك تربح وأنت نائم:</p>

<h3>1. ابحث عن "الوجع" وليس "الإعجاب"</h3>
<p>لا تسأل الناس: "هل تعجبكم هذه الفكرة؟" (الكل سيجامل بكلمة نعم). اسأل نفسك: "ما هو الشيء الذي يجعل الناس يسهرون قلقين في الليل؟".</p>
<ul>
    <li>إذا كان جمهورك مبرمجين، فـ "الوجع" هو (كيف أجتاز مقابلة جوجل؟) وليس (أساسيات لغة بايثون).</li>
    <li>إذا كان جمهورك في سوريا، فـ "الوجع" هو (كيف أستلم أرباحي بالدولار؟) وليس (مقدمة في التجارة الإلكترونية).</li>
</ul>

<h3>2. قاعدة الـ 10% (The 10% Rule)</h3>
<p>لا تحاول تعليم الناس "كل شيء". المدرب الذكي هو من يحل 10% فقط من المشكلة الكبيرة، لكنه يحلها بجودة 100%.</p>
<p>بدل "دورة كاملة عن التصميم"، اصنع "قالب بورتفوليو احترافي للمصممين". أيهما أسرع في البيع؟ طبعاً القالب.</p>

<h3>3. "البرهان" قبل "البيان"</h3>
<p>الناس في 2026 لا يشترون وعوداً. إذا أردت بيع منتج رقمي، ابدأ بتقديم "نتيجة" مجانية صغيرة.</p>
<p>مقال تعليمي واحد يحل مشكلة تقنية معقدة، سيبني لك ثقة تعادل حملة إعلانية بآلاف الدولارات.</p>

<hr />
<p>المنصات (مثل منصتي الرقمية وغيرها) هي مجرد "أدوات". السر الحقيقي ليس في الأداة التيستخدمها، بل في "القيمة" التي تضعها داخل هذه الأداة. ابدأ بالبحث عن "الثغرة" في حياة جمهورك، وسيكون منتجك هو "الجسر" الذي يحتاجونه.</p>
                `,
                excerpt: "الفكرة ليست هي الكنز.. الاحتياج هو الكنز الحقيقي. تعلم كيف تكتشف ما يحتاجه جمهورك فعلياً وتحوله إلى أرباح مستدامة.",
                coverImage: "https://images.unsplash.com/photo-1519389950473-47ba0277781c?w=1200",
                category: "تحليلات",
                authorName: "ماهر",
                status: "PUBLISHED",
                userId: admin.id
            }
        });

        return NextResponse.json({ success: true, slug: post.slug });
    } catch (e: any) {
        return NextResponse.json({ error: e.message }, { status: 500 });
    }
}
